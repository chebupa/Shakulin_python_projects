# Даны средние значения температур за каждый месяц в году.
# Найти минимальное и максимальное значения температур за год.
# Вывести значения температур по временам года.


# Средние значения температур по месяцам (в порядке от января до декабря)
temperatures = [-5.2, -3.0, 2.5, 9.3, 15.2, 20.1, 22.3, 21.8, 16.4, 9.0, 2.1, -2.5]


# Минимальная и максимальная температура за год
min_temp = min(temperatures)
max_temp = max(temperatures)
print(f"Минимальная температура за год: {min_temp}°C")
print(f"Максимальная температура за год: {max_temp}°C")


# Времена года
seasons = {
    "Зима": [temperatures[11], temperatures[0], temperatures[1]],  # декабрь, январь, февраль
    "Весна": temperatures[2:5],  # март, апрель, май
    "Лето": temperatures[5:8],   # июнь, июль, август
    "Осень": temperatures[8:11]  # сентябрь, октябрь, ноябрь
}
print("\nТемпературы по временам года:")
for season, temps in seasons.items():
    print(f"{season}: {temps}")